#!/usr/bin/env node
/**
 * Generates src/data/wargearNames.js — the seed dictionary of full 11th-edition
 * item names, keyed by faction, used by the army expander to reverse the
 * compactor's algorithmically-generated abbreviations. Emitted as an ES module
 * (not .json) so both Vite and the Node CLI import it without JSON attributes.
 *
 * Sources:
 *   1. node_modules/40k-compactor/skippable_wargear.json  (real per-faction weapon names)
 *   2. A curated list of universal wargear + common enhancements (below).
 *
 * The reverse index itself is built at runtime (see wargearDictionary.js); this
 * file only supplies the corpus of known full names. Re-run after upgrading the
 * 40k-compactor dependency:  node scripts/gen-wargear-dict.mjs
 */
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '..');
const skippablePath = resolve(repoRoot, 'node_modules/40k-compactor/skippable_wargear.json');
const outPath = resolve(repoRoot, 'src/data/wargearNames.js');

// Universal / cross-faction wargear that is frequently a *chosen* upgrade (so it
// survives into the compacted output as an abbreviation and must be reversible).
const COMMON = [
  'Bolt pistol', 'Boltgun', 'Bolt rifle', 'Heavy bolt pistol', 'Plasma pistol', 'Plasma gun',
  'Plasma incinerator', 'Power fist', 'Power weapon', 'Power sword', 'Power maul', 'Chainsword',
  'Chainblade', 'Combi-weapon', 'Combi-bolter', 'Storm bolter', 'Storm shield', 'Meltagun',
  'Multi-melta', 'Flamer', 'Heavy flamer', 'Grav-gun', 'Grav-pistol', 'Lascannon', 'Autocannon',
  'Missile launcher', 'Heavy bolter', 'Twin heavy bolter', 'Twin lascannon', 'Thunder hammer',
  'Lightning claw', 'Relic blade', 'Master-crafted power weapon', 'Close combat weapon',
  'Frag grenades', 'Krak grenades', 'Grenades', 'Force weapon', 'Force sword', 'Force stave',
  'Icon of Khorne', 'Icon of Nurgle', 'Icon of Slaanesh', 'Icon of Tzeentch', 'Icon of Chaos Ascendant',
  'Icon of Chaos', 'Havoc launcher', 'Armoured tracks', 'Hunter-killer missile', 'Combi-flamer',
  'Combi-plasma', 'Combi-melta', 'Combi-grenade launcher', 'Autopistol', 'Chainblades',
  'Magma cutter', 'Mauler chainblade', 'Khornate eviscerator', 'Great cleaver of Khorne',
  'Skullsmasher and mangler', 'Hideous mutations', 'Daemonic claws',
];

// A modest seed of enhancement names by faction (compacted output tags these "E: <abbr>").
// Extend freely — anything unresolved is preserved verbatim with its point cost.
const ENHANCEMENTS = {
  'World Eaters': ['Battle-lust', 'Berzerker Glaive', 'Favoured of Khorne', 'Talisman of Burning Blood', 'The Eightfold Blade'],
  'Space Marines': ['Artificer Armour', 'The Honour Vehement', 'Adept of the Codex', 'Fire Discipline', 'War-tempered Artifice'],
};

function harvest(skippable) {
  const factions = {};
  for (const [faction, unitMap] of Object.entries(skippable)) {
    const names = new Set();
    if (unitMap && typeof unitMap === 'object') {
      for (const val of Object.values(unitMap)) {
        if (Array.isArray(val)) {
          for (const w of val) if (typeof w === 'string' && w.trim()) names.add(w.trim());
        }
      }
    }
    factions[faction] = [...names].sort();
  }
  return factions;
}

const skippable = JSON.parse(readFileSync(skippablePath, 'utf8'));
const factions = harvest(skippable);

const out = {
  generatedFrom: '40k-compactor/skippable_wargear.json + curated common list',
  note: 'Corpus of known full item names per faction; abbreviation reverse-index is built at runtime.',
  common: [...new Set(COMMON)].sort(),
  enhancements: ENHANCEMENTS,
  factions,
};

mkdirSync(dirname(outPath), { recursive: true });
const banner =
  '// AUTO-GENERATED by scripts/gen-wargear-dict.mjs — do not edit by hand.\n' +
  '// Re-run `node scripts/gen-wargear-dict.mjs` after upgrading 40k-compactor.\n';
writeFileSync(outPath, banner + 'export default ' + JSON.stringify(out, null, 2) + ';\n');

const factionCount = Object.keys(factions).length;
const nameCount = Object.values(factions).reduce((n, a) => n + a.length, 0);
console.log(`Wrote ${outPath}`);
console.log(`  factions: ${factionCount}, harvested names: ${nameCount}, common: ${out.common.length}`);
